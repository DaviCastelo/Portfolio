export function filterTechStackOptions(
  query: string,
  selected: string[],
  limit = 80
): string[] {
  const q = query.trim().toLowerCase();
  const selectedLower = new Set(selected.map((s) => s.toLowerCase()));

  return TECH_STACK_OPTIONS.filter((tech) => {
    if (selectedLower.has(tech.toLowerCase())) return false;
    if (!q) return true;
    return tech.toLowerCase().includes(q);
  }).slice(0, limit);
}

export function normalizeTechLabel(label: string): string {
  const trimmed = label.trim();
  if (!trimmed) return "";

  const match = TECH_STACK_OPTIONS.find(
    (t) => t.toLowerCase() === trimmed.toLowerCase()
  );
  return match ?? trimmed;
}
